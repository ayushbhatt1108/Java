public class ForLoop{

    public static void main(String[] args) {
        // Loop from 1 to 5
        for (int i = 1; i <= 5; i++) {
            System.out.println("Value of i: " + i);
        }
    }
}

//Explanation:
//int i = 1 — initialization: start with i = 1.

//i <= 5 — condition: keep running the loop while i is less than or equal to 5.

//i++ — update: after each loop, increase i by 1.
